Обычно полки крепятся к стенам помещения. В зависимости от способа крепления полок необходимо подобрать готовую фурнитуру по каталогам. Стандартно полки крепятся к стенам, к зеркалам и другим поверхностям с помощью зажимных креплений из алюминия, нержавеющей стали и пластика. Полки, устанавливаемые в шкафах, могут иметь специальные крепления для УФ-склеивания. 

