Полки из стекла могут быть подвешены с помощью тросовой системы или системы вертикальных штанг. На тросах или штангах имеются ограничители, на которые опираются полки. С помощью ограничителей высота полок может быть изменена в любое время. Полки из стекла закрепленные к штанге могут поворачиваться вокруг оси штанги (барные и выставочные полки).

