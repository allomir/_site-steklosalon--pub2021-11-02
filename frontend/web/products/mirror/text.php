Наиболее распространенным способом декорирования зеркала является обрамление зеркала в багет. Багеты для зеркал имеют множество вариантов и подбираются по каталогам. Багеты отличаются материалом, толщиной, узором и цветом.  Для багетов используется разный материал: дерево, пластик, металл и алюминий. Пластиковые, металлические багеты долговечны и не боятся влаги. Пластиковые багеты нашли наибольшее распространение из-за низкой стоимости.

