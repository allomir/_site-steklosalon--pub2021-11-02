Виды декорирования зеркал:
Создание рамки для зеркала из багетов, багеты подбираются на выбор по каталогам.
Декорирование с помощью создания фацета, применяется для зеркал с толщиной полотна от 6 мм.
Сатинирование (матирование) для нанесения монотонного рисунка.
Фотопечать по зеркалу для нанесения цветного изображения или фотографии.
Покраска для нанесения цветных или монотонных рисунков.
